public class Person {
    public String imie;
    public String nazwisko;
    public String nrtel;
    public String adress;
    public String email;

    public Person(String imie, String nazwisko, String nrtel, String adress, String email) {
        this.imie = imie;
        this.nazwisko = nazwisko;
        this.nrtel = nrtel;
        this.adress = adress;
        this.email = email;
    }
}
